fun main() {
    // Comparison operators
    // >, <, >=, <=, ==, !=
    // > greater than
    // < less than
    // >= greater than or equal to
    // <= less than or equal to
    // == equal to
    // != not equal to

    val a = 5
    val b = 3
    // a > b = true
    println(a > b)

    // a < b = false
    println(a < b)

    // a >= b = true
    println(a >= b)

    // a <= b = false
    println(a <= b)

    // a == b = false
    println(a == b)

    // a != b = true
    println(a != b)

}